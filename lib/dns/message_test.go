func TestMessageIsExpired(t *testing.T) {
	cases := []struct {
		desc string
		msg  *Message
		exp  bool
	}{{
		desc: "Message is not expired",
		msg: &Message{
			Answer: []ResourceRecord{{
				TTL: 1,
			}},
		},
		exp: false,
	}, {
		desc: "Message is expired",
		msg: &Message{
			Answer: []ResourceRecord{{
				TTL: 0,
			}},
		},
		exp: true,
	}}

	for _, c := range cases {
		t.Log(c.desc)

		got := c.msg.IsExpired()

		test.Assert(t, "IsExpired", c.exp, got, true)
	}
}

func TestMessagePackDomainName(t *testing.T) {
	cases := []struct {
		in  []byte
		exp []byte
	}{{
		in:  []byte("a.b"),
		exp: []byte{1, 'a', 1, 'b', 0},
	}, {
		in:  []byte("a.b."),
		exp: []byte{1, 'a', 1, 'b', 0},
	}, {
		in:  []byte("a\\.b.c"),
		exp: []byte{3, 'a', '.', 'b', 1, 'c', 0},
	}, {
		in:  []byte("a\\065.b.c"),
		exp: []byte{2, 'a', 'a', 1, 'b', 1, 'c', 0},
	}, {
		in:  []byte("a\\065"),
		exp: []byte{2, 'a', 'a', 0},
	}}

	msg := NewMessage()
	msg.dnameOff = make(map[string]uint16)

	for _, c := range cases {
		msg.Reset()
		msg.Packet = msg.Packet[:0]

		msg.packDomainName(c.in, false)

		test.Assert(t, "packDomainName", c.exp, msg.Packet, true)
	}
}

func TestMessagePackQuestion(t *testing.T) {
	cases := []struct {
		desc string
		msg  *Message
		exp  []byte
	}{{
		desc: "Empty name",
		msg: &Message{
			Question: SectionQuestion{
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x00, 0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Single domain name",
		msg: &Message{
			Question: SectionQuestion{
				Name:  "kilaBit",
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't', 0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Two domain names",
		msg: &Message{
			Question: SectionQuestion{
				Name:  "kilabit.info",
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't',
			0x04, 'i', 'n', 'f', 'o',
			0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}, {
		desc: "Three domain names",
		msg: &Message{
			Question: SectionQuestion{
				Name:  "MAIL.KILABIT.INFO",
				Type:  QueryTypeA,
				Class: QueryClassIN,
			},
			dnameOff: make(map[string]uint16),
		},
		exp: []byte{
			0x04, 'm', 'a', 'i', 'l',
			0x07, 'k', 'i', 'l', 'a', 'b', 'i', 't',
			0x04, 'i', 'n', 'f', 'o',
			0x00,
			0x00, 0x01, 0x00, 0x01,
		},
	}}

	for _, c := range cases {
		t.Log(c.desc)

		c.msg.packQuestion()

		test.Assert(t, "packet", c.exp, c.msg.Packet, true)
	}
}

